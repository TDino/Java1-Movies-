   private Optional<User> loginUser() {
        if(!tfUsername.getText().trim().isEmpty()&&!tfPassword.getText().trim().isEmpty()){
            try {
                Optional<User> u = this.repo.Login(tfUsername.getText().trim(), tfPassword.getText().trim());
                if(u.isPresent())
                    return u;     
                
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("error", ex.getMessage());
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            MessageUtils.showErrorMessage("Failed to login", "Type in username and password");
        }
        return Optional.empty();
    }
