    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateForm()){
                try {
                    repo.register(tfUsername.getText().trim(),tfPassword.getText().trim());
                } catch (Exception ex) {
                    Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                MessageUtils.showInformationMessage("success", tfUsername.getText().trim() + " is registered");
                
                tfUsername.setText("");
                tfPassword.setText("");

        }else{
            MessageUtils.showErrorMessage("error", "insert data");
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

